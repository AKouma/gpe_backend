package com.etna.gpe.dto;

public class HomeDto {

	private int particularNumber;
	private int organizationNumber;
	private int eventNumber;
	
	public int getParticularNumber() {
		return particularNumber;
	}
	public void setParticularNumber(int particularNumber) {
		this.particularNumber = particularNumber;
	}
	public int getOrganizationNumber() {
		return organizationNumber;
	}
	public void setOrganizationNumber(int organizationNumber) {
		this.organizationNumber = organizationNumber;
	}
	public int getEventNumber() {
		return eventNumber;
	}
	public void setEventNumber(int eventNumber) {
		this.eventNumber = eventNumber;
	}
	
	
}
